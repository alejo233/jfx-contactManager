package model;

public class Contact {
	
	private String name;
	private String email;
	
	
	public Contact(String nm, String em) {
		nm = name;
		em = email;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getEmail() {
		return email;
	}


	public void setEmail(String email) {
		this.email = email;
	}
	
	
	

}
